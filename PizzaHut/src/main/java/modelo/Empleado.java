package modelo;

/**
 *
 * @author PC-EDUARDO
 */
public class Empleado {
    
    private int IDEMP;
    private String NOMEMP;
    private String CELEMP;
    private String COREMP;
    private int IDCAR;
    

    /**
     * @return the IDEMP
     */
    public int getIDEMP() {
        return IDEMP;
    }

    /**
     * @param IDEMP the IDEMP to set
     */
    public void setIDEMP(int IDEMP) {
        this.IDEMP = IDEMP;
    }

    /**
     * @return the NOMEMP
     */
    public String getNOMEMP() {
        return NOMEMP;
    }

    /**
     * @param NOMEMP the NOMEMP to set
     */
    public void setNOMEMP(String NOMEMP) {
        this.NOMEMP = NOMEMP;
    }

    /**
     * @return the CELEMP
     */
    public String getCELEMP() {
        return CELEMP;
    }

    /**
     * @param CELEMP the CELEMP to set
     */
    public void setCELEMP(String CELEMP) {
        this.CELEMP = CELEMP;
    }

    /**
     * @return the COREMP
     */
    public String getCOREMP() {
        return COREMP;
    }

    /**
     * @param COREMP the COREMP to set
     */
    public void setCOREMP(String COREMP) {
        this.COREMP = COREMP;
    }

    /**
     * @return the IDCAR
     */
    public int getIDCAR() {
        return IDCAR;
    }

    /**
     * @param IDCAR the IDCAR to set
     */
    public void setIDCAR(int IDCAR) {
        this.IDCAR = IDCAR;
    }
    
}
